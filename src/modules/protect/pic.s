init_pic:
        ;-----------------------------------------
        ; 【レジスタの保存】
        ;-----------------------------------------
        push eax

        ;-----------------------------------------
        ; マスタの設定
        ;-----------------------------------------
        outp 0x20, 0x11
        outp 0x21, 0x20
        outp 0x21, 0x04
        outp 0x21, 0x05
        outp 0x21, 0xFF

        ;-----------------------------------------
        ; スレーブの設定
        ;-----------------------------------------
        outp 0xA0, 0x11
        outp 0xA1, 0x28
        outp 0xA1, 0x02
        outp 0xA1, 0x01
        outp 0xA1, 0xFF

        ;-----------------------------------------
        ; 【レジスタの復帰】
        ;-----------------------------------------
        pop eax

        ret
