call_gate:
        ;-----------------------------------------
        ; 【スタックフレームの構築】
        ;-----------------------------------------
        push ebp
        mov ebp, esp

        ;-----------------------------------------
        ; 【レジスタの保存】
        ;-----------------------------------------
        pusha
        push ds
        push es

        ;-----------------------------------------
        ; データ用セグメントの設定
        ;-----------------------------------------
        mov ax, 0x0010
        mov ds, ax
        mov es, ax

        ;-----------------------------------------
        ; 文字を表示
        ;-----------------------------------------
        mov eax, dword [ebp + 12]
        mov ebx, dword [ebp + 16]
        mov ecx, dword [ebp + 20]
        mov edx, dword [ebp + 24]
        cdecl draw_str, eax, ebx , ecx, edx

        ;-----------------------------------------
        ; 【レジスタの復帰】
        ;-----------------------------------------
        pop es
        pop ds
        popa

        ;-----------------------------------------
        ; 【スタックフレームの破棄】
        ;-----------------------------------------
        mov esp, ebp
        pop ebp

        retf 4 * 4
